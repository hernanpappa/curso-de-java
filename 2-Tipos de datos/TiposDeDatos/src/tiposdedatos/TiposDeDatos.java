public class TiposDeDatos {

    public static void main(String[] args) {
        
        /**
         * Java define 8 tipos de datos (primitivos)
         * 
         * Enteros: Nos permiten trabajar con numeros enteros positivos o negativos 
         *      byte
         *      short
         *      int
         *      long
         * 
         * Decimales: Nos permiten trabajar con datos con punto decimal positivos o negativos
         *      float
         *      double
         * 
         * Caracteres: Nos permiten almacenar simbolos
         *      char: almacena un simbolo
         * 
         * Boolean:
         *      boleean: almacena verdadero o falso
         * */
        
        int versionJava = 9;
        
        float versionEspecifica = 9.8f; //Los numeros flotantes deben terminar con el subfijo f
        
        char letra = 'a';
        char numero = 97;

        
        System.out.println(letra);
        System.out.println(numero);
        
        boolean valor1 = true;
        boolean valor2 = false;
        
    }
    
}
